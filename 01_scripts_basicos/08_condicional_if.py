"""
Script: Condicional if
Objetivo:
- Tomar decisiones en el programa
- Comparar valores
- Usar if / elif / else
"""

edad = int(input("Ingrese su edad: "))

if edad >= 18:
    print("Sos mayor de edad")
elif edad >= 13:
    print("Sos adolescente")
else:
    print("Sos menor de edad")

"""
Conceptos clave:
- if: condición principal
- elif: condición alternativa
- else: caso por defecto
"""
