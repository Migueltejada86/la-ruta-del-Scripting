"""
Script: Función con return
Objetivo:
- Devolver valores desde una función
- Separar lógica de salida
"""

def sumar(a, b):
    resultado = a + b
    return resultado

total = sumar(5, 3)

print("El resultado es:", total)

"""
Diferencia clave:
- print muestra algo
- return devuelve un valor usable
"""
