"""
Script: Bucle while
Objetivo:
- Repetir mientras se cumpla una condición
- Controlar la ejecución
"""

contador = 0

while contador < 5:
    print("Contador:", contador)
    contador += 1

print("Fin del bucle")

"""
Notas:
- while se ejecuta mientras la condición sea True
- cuidado con los bucles infinitos
"""
