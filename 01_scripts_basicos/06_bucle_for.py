"""
Script: Bucle for básico
Objetivo:
- Entender qué es un bucle
- Repetir acciones automáticamente
- Usar range()
"""

# Un bucle for repite un bloque de código
# range(5) genera los números: 0,1,2,3,4

for i in range(5):
    print("Iteración", i)

"""
Qué está pasando:
- i toma un valor distinto en cada vuelta
- El código dentro del for se ejecuta 5 veces
"""
